function dcdt = ODE7(t, c, p)

p_c = p(1,:);
a_c = p(2,:);

k_p = p(3,1);
k_a = p(3,2);
d_plt = p(3,3);
k_decline = p(3,4);
k_shed = p(3,5);


% p_c = [7.6*10^(-3), 3.96*10^(-2), 0.04, 0.56, 0.32, 0.7, 1];
% a_c = [0.77, 0.7689, 0.7359, 0.7678, 0.154, 0.11, 0.055];
% d_platelets = 0.6925;
% k_p = 6*10^(-9);
% k_a = 12.8*10^(-10);
% k_frac = 1000;

% s_p = 1/(1+k_p*(c(5,1)+c(6,1)+c(7,1)+c(8,1)));
% s_a = 1/(1+k_a*(c(5,1)+c(6,1)+c(7,1)+c(8,1)));
s_p = 1/(1+k_p*(c(7,1)+c(8,1)));
s_a = 1/(1+k_a*(c(7,1)+c(8,1)));
% k_a_1 = k_a*1.39/4*10^4;
% % k_a_2 = 2.5*10^(-7);
% k_p_1 = k_p*1.39/4*10^4;
% s_a_local = 1/(1+k_a_1*c(1,1));
% s_p_local = 1/(1+k_p_1*c(1,1));
% s_a_local_2 = 1/(1+k_a_1*c(2,1));
% s_p_local_2 = 1/(1+k_p_1*c(2,1));

dcdt = zeros(7,1);

dcdt(1,1) = (2*a_c(1)*s_a - 1)*p_c(1)*s_p*c(1,1);
dcdt(2,1) = (2*a_c(2)*s_a  - 1)*p_c(2)*s_p*c(2,1) + 2*(1-a_c(1)*s_a)*p_c(1)*s_p*c(1,1);
dcdt(3,1) = (2*a_c(3)*s_a - 1)*p_c(3)*s_p*c(3,1) + 2*(1-a_c(2)*s_a)*p_c(2)*s_p*c(2,1);
dcdt(4,1) = (2*a_c(4)*s_a - 1)*p_c(4)*s_p*c(4,1) + 2*(1-a_c(3)*s_a)*p_c(3)*s_p*c(3,1);
dcdt(5,1) = 2*(1-a_c(4)*s_a)*p_c(4)*s_p*c(4,1) - p_c(5)*c(5,1);
dcdt(6,1) = p_c(5)*c(5,1)-p_c(6)*c(6,1);
dcdt(7,1) = k_shed*p_c(6)*c(6,1) - d_plt*c(7,1);
dcdt(8,1) = -k_decline*c(8,1);

end